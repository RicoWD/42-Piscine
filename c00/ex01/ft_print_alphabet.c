#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_alphabet(void)
{
	int	current;

	current = 'a';
	while (current <= 'z')
	{
		ft_putchar(current);
		current++;
	}
}
