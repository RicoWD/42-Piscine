char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (i < n && src[i])
	{
		dest[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}

#include <stdio.h>

int	main(void)
{
	char	dest[] = "Sara";
	char	src[] = "hello";
//	unsigned int	n = 3;

	printf("%s\n", ft_strncpy(dest, src, 3));
}
